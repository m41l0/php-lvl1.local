<h2>Задание 6</h2>
<pre>
Объявите массив, индексами которого являются буквы русского языка, а
значениями – соответствующие латинские буквосочетания (‘а’=> ’a’, ‘б’ => ‘b’, ‘в’
=> ‘v’, ‘г’ => ‘g’, …, ‘э’ => ‘e’, ‘ю’ => ‘yu’, ‘я’ => ‘ya’).
Напишите функцию транслитерации строк.
</pre>
<p>
<h4>Ответ:</h4>
<?php
function translit($str) {
    $alphabet = ['а' => 'a', 'б' => 'b', 'в' => 'v', 'г' => 'g', 'д' => 'd', 'е' => 'e', 'ё' => 'yo', 'ж' => 'zh',
        'з' => 'z', 'и' => 'i', 'й' => 'yi', 'к' => 'k', 'л' => 'l', 'м' => 'm', 'н' => 'n', 'о' => 'o', 'п' => 'p',
        'р' => 'r', 'с' => 's', 'т' => 't', 'у' => 'u', 'ф' => 'f', 'х' => 'kh', 'ц' => 'ts', 'ч' => 'ch', 'ш' => 'sh',
        'щ' => 'shch', 'ъ' => '`', 'ы' => 'y', 'ь' => '`', 'э' => 'ye', 'ю' => 'yu', 'я' => 'ya'];

    for($i=0;$i<strlen($str);$i++){
        if( (ord($str[$i]) >= 128) && (ord($str[$i]) <= 255) ){
            return strtr(mb_strtolower($str), $alphabet);
        } else {
            return strtr(strtolower($str), array_flip($alphabet));
        }
    }
}
$str = 'Ученье - свет, а неученье - тьма.';
$strEng = 'Uchen`e - svet, a neuchen`e - t`ma.';
echo translit($str) . '<br>';
echo translit($strEng);
?>

<h2>Задание 7</h2>
<pre>
Напишите функцию, которая заменяет в строке пробелы на подчеркивания и
возвращает видоизмененную строчку.
</pre>
<p>
<h4>Ответ:</h4>
<?php
function underline($str) {
    $space = ' ';
    $underline = '_';
    return str_replace($space, $underline, $str);
}
$string = 'Выпей чайку - забудешь тоску.';
echo underline($string);
?>
